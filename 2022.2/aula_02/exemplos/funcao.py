# Função com retorno e parâmetros
def junta_nome(nome, sobrenome):
    nome_completo = nome + " " + sobrenome
    return nome_completo

primeiro_nome = "Messias"
ultimo_nome = "De La Mancha"

print(junta_nome(primeiro_nome, ultimo_nome))

# Funçãom com retorno sem parâmetros
def somar_10_e_10():
    return 10 + 10

print(somar_10_e_10())

