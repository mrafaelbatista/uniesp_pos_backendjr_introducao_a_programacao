'''Escreva um programa em Python que peça ao usuário
para digitar dois números e exiba a soma deles.'''

# Solicitando a digitação do primeiro número
numero1 = int(input('Digite seu primeiro número inteiro: '))

# Solicitando a digitação do segundo número
numero2 = int(input('Digite seu segundo número inteiro :'))

# Realizar a soma de numero1 + numero2
resultado_soma = numero1 + numero2

# Exibir o resultado da minha soma
print(f'O resultado da soma é {resultado_soma}')