# Definição de uma variável do tipo String
professor = "severus snape"

# Uso da função title() de uma String
print(professor.title())
print('marcelo fernandes'.title())
# Uso da função upper() de uma String
print(professor.upper())
print('marcelo fernandes'.upper())
# Uso da função lower() de uma String
print(professor.lower())
print('MARCELO FERNANDES'.lower())

