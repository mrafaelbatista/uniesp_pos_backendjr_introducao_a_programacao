# Exercício do Cálculo de Área
# Escreva um programa em Python que solicite ao usuário a largura
# e a altura de um retângulo. Calcule a área desse retângulo e
# exiba o resultado.

# Solicita a largura do retângulo
largura = float(input("Digite a largura do retângulo: "))

# Solicita a altura do retângulo
altura = float(input("Digite a altura do retângulo: "))

# Calcula a área do retângulo
area = largura * altura

# Exibe o resultado
print(f"A área do retângulo é: {area:.2f}")
